    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String teamID = txtTeamID.getText();
        ArrayList <TeamsModel> list = TeamsDao.search(teamID);
        Iterator<TeamsModel> itr = list.iterator();
        while (itr.hasNext()) {
            TeamsModel teamsObj = itr.next();
            txtTeamID.setText(teamsObj.getTeamID());
            txtTeamName.setText(teamsObj.getTeamName());
            txtCoachID.setSelectedItem(teamsObj.getCoachID());
            txtSportsName.setSelectedItem(teamsObj.getSportsName());
        }
        btnInsert.setEnabled(true);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_btnSearchActionPerformed

