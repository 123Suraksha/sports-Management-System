    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String playerID = model.getValueAt(index, 0).toString();
        String playerName = model.getValueAt(index, 1).toString();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to Delete " + playerName, "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            PlayersDao.delete(playerID);
            clear();
        }
    }//GEN-LAST:event_jTable1MouseClicked

