    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        ArrayList<TeamsModel> list = TeamsDao.getAllRecords();
        Iterator<TeamsModel> itr = list.iterator();
        while (itr.hasNext()) {
            TeamsModel teamsObj = itr.next();
            txtTeamID.addItem(teamsObj.getTeamID());
        }
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<PlayersModel> list2 = PlayersDao.getAllRecords();
        Iterator<PlayersModel> itr2 = list2.iterator();
        while (itr2.hasNext()) {
            PlayersModel playersObj = itr2.next();
            dtm.addRow(new Object[]{playersObj.getPlayerID(), playersObj.getPlayerName(), playersObj.getGender(), playersObj.getHeight(), playersObj.getWeight(), playersObj.getTeamID()});
        }
    }//GEN-LAST:event_formComponentShown

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String playerID = txtPlayerID.getText();
        ArrayList <PlayersModel> list = PlayersDao.search(playerID);
        Iterator<PlayersModel> itr = list.iterator();
        while (itr.hasNext()) {
            PlayersModel playersObj = itr.next();
            txtPlayerID.setText(playersObj.getPlayerID());
            txtPlayerName.setText(playersObj.getPlayerName());
            txtGender.setSelectedItem(playersObj.getGender());
            txtHeight.setText(Integer.toString(playersObj.getHeight()));
            txtWeight.setText(Float.toString(playersObj.getWeight()));
            txtTeamID.setSelectedItem(playersObj.getTeamID());
        }
        btnInsert.setEnabled(true);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_btnSearchActionPerformed

