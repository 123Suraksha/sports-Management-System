    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        ArrayList<CoachModel> list = CoachDao.getAllRecords();
        Iterator<CoachModel> itr = list.iterator();
        while (itr.hasNext()) {
            CoachModel coachObj = itr.next();
            txtCoachID.addItem(coachObj.getCoachID());
            txtSportsName.addItem(coachObj.getSportsName());
        }
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<TeamsModel> list2 = TeamsDao.getAllRecords();
        Iterator<TeamsModel> itr2 = list2.iterator();
        while (itr2.hasNext()) {
            TeamsModel teamsObj = itr2.next();
            dtm.addRow(new Object[]{teamsObj.getTeamID(), teamsObj.getTeamName(), teamsObj.getCoachID(), teamsObj.getSportsName()});
        }
    }//GEN-LAST:event_formComponentShown

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String teamID = model.getValueAt(index, 0).toString();
        String teamName = model.getValueAt(index, 1).toString();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to Delete " + teamName, "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            TeamsDao.delete(teamID);
            clear();
        }
    }//GEN-LAST:event_jTable1MouseClicked

